
# 창ngulos e seu seno, cosseno e tangente

import math

angulo = float(input('Digite o valor do 창ngulo: '))
sen = math.sin(math.radians(angulo))
cos = math.cos(math.radians(angulo))
tan = math.cos(math.radians(angulo))


print('O seno, o cosseno e a tangente desse 창ngulo s찾o, respectivamente; {:.3f},{:.3f},{:.3f}'.format(sen,cos,tan))




